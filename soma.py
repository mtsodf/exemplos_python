# Esse programa cria duas variáveis a e b, atribui valores a elas e depois soma esses valores.
# O valor da soma é impresso na tela.

# Atribui o valor 10 à variável 'a'
a = 10

# Atribui o valor 5 à variável 'b'
b = 5

# Soma os valores das variáveis 'a' e 'b' e armazena o resultado na variável 'soma'
soma = a + b

# Imprime o valor da soma na tela
print("O valor da soma é", soma)
